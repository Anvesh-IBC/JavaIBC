package com.example.demos;

public class WrapperOverviewDemo {
	public static void main(String[] args) {
		// For each primitive: immutable wrapper class
		System.out.println(Boolean.class + " wraps boolean");
		System.out.println(Byte.class + " wraps byte");
		System.out.println(Short.class + " wraps short");
		System.out.println(Integer.class + " wraps int");
		System.out.println(Long.class + " wraps long");
		System.out.println(Float.class + " wraps float");
		System.out.println(Double.class + " wraps double");
		System.out.println(Character.class + " wraps char");
		System.out.println(Void.class + " (marker for void)");
	}
}

package VariablesInitialization;

public class VariablesDemo {
	public static void main(String[] args) {
		int age = 20; // Number locker for age
		double height = 5.9; // For height (with decimals)
		char gender = 'M'; // For single character (Male)
		boolean isStudent = true; // For yes/no (true means yes)
		String name = "Ravi"; // For text like a person’s name

		System.out.println("Name: " + name);
		System.out.println("Age: " + age + ", Height: " + height);
		System.out.println("Gender: " + gender + ", Student: " + isStudent);
	}
}

package LiteralsPrimitiveTypes;

public class LiteralAssignmentDemo {
	public static void main(String[] args) {
		int age = 25; // 25 is a literal integer
		char grade = 'A'; // 'A' is a literal character
		boolean isActive = true; // true is a literal boolean
		System.out.println("Age: " + age);
		System.out.println("Grade: " + grade);
		System.out.println("Is active? " + isActive);
	}
}

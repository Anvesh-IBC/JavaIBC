package LiteralsPrimitiveTypes;

public class PrimitiveLiterals {
	public static void main(String[] args) {
		byte smallCount = 12;
		short shortNum = 30000;
		int population = 1400000000;
		long bigNum = 9876543210L;
		float price = 261.75f;
		double distance = 45678.123;
		char grade = 'A';
		boolean isIndian = true;

		System.out.println("byte: " + smallCount);
		System.out.println("short: " + shortNum);
		System.out.println("int: " + population);
		System.out.println("long: " + bigNum);
		System.out.println("float: " + price);
		System.out.println("double: " + distance);
		System.out.println("char: " + grade);
		System.out.println("boolean: " + isIndian);
	}
}